
function toTimestamp(strDate){
	var datum = Date.parse(strDate);
	return datum/1000;
}

$.getJSON('https://apiv2.bitcoinaverage.com/indices/global/history/BTCEUR?format=json', function (data) {

    // split the data set into ohlc and volume
    var ohlc = [],
    volume = [],
    dataLength = data.length,
    // set the allowed units for data grouping
    groupingUnits = [[
        'week',                         // unit name
        [1]                             // allowed multiples
        ], [
        'month',
        [1, 2, 3, 4, 6]
        ]],

        i = 0;
        function Comparator(a, b) {
          if (a.time < b.time) return -1;
          if (a.time > b.time) return 1;
          return 0;
        }

        data = data.sort(Comparator);

        for (i; i < dataLength; i += 1) {
          ohlc.push([
            toTimestamp(data[i].time)*1000, // the date
            data[i].average, // open
            ]);

          volume.push([
            toTimestamp(data[i].time)*1000, // the date
            data[i].volume// the volume
            ]);
        }

      // create the chart
      Highcharts.stockChart('chart', {

        rangeSelector: {
          selected: 1
        },

        title: {
          text: 'Historique bitcoin'
        },

        yAxis: [{
          labels: {
            align: 'right',
            x: -3
          },
          title: {
            text: 'OHLC'
          },
          height: '60%',
          lineWidth: 2
        }, {
          labels: {
            align: 'right',
            x: -3
          },
          title: {
            text: 'Volume'
          },
          top: '65%',
          height: '35%',
          offset: 0,
          lineWidth: 2
        }],

        tooltip: {
          split: true
        },

        series: [{
          type: 'area',
          name: 'Bitcoin',
          data: ohlc,
          dataGrouping: {
            units: groupingUnits
          }
        }, {
          type: 'column',
          name: 'Volume',
          data: volume,
          yAxis: 1,
          dataGrouping: {
            units: groupingUnits
          }
        }]
    });
});
